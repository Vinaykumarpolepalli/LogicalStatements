package com.logicalstatement;

public class ArithemticOperator {

	public static void main(String[] args) {
	    int a = 10;
	    int b = 20;
	     
	    Double d = 100.0;
	    Double d1 = 200.0;
	    
	    
	    System.out.println(a+b);
	    System.out.println(a-b);
	    System.out.println(a*b);
	    System.out.println(a/b);
	    System.out.println(a%b);
	    
	    System.out.println("**********************************");
	    System.out.println(d+d1);
	    System.out.println(d-d1);
	    System.out.println(d*d1);
	    System.out.println(d/d1);
	    System.out.println(a%d1);
	    

	}

}
