package com.logicalstatement;

public class SwappingWith {
  public static void main(String[] args) {
	
	  
	  int a = 30;
	  int b = 80;
	  int c = 0;
	  
	  c = a;
	  a = b;
	  b = c;
	  
	  System.out.println("A numbers is  = "+a);
	  System.out.println("B numbers is = "+b);
}
}
